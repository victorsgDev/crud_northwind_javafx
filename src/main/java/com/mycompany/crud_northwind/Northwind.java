private void table_update() {
        int C;
        try {
            Class.forName("com.mysql.jdbc.Driver");
            con = DriverManager.getConnection("jdbc:mysql://localhost:3306/northwind","root","");
            insert = con.prepareStatement("SELECT * FROM products");
            Rs = insert.executeQuery();
            
            ResultSetMetaData RSMD = Rs.getMetaData(); // Analiza la estructura de la tabla
            C = RSMD.getColumnCount();
            DefaultTableModel DFT = (DefaultTableModel) jTable2.getModel(); // Recoge el modelo de la tabla a atacar (vector de vectores)
            DFT.setRowCount(0);
            while (Rs.next()) {
                Vector v2 = new Vector();
           
                for (int i = 1; i <= C; i++) {
                    v2.add(Rs.getInt("ProductID"));
                    v2.add(Rs.getString("ProductName"));
                    v2.add(Rs.getString("QuantityPerUnit"));
                    v2.add(Rs.getDouble("UnitPrice"));
                }
                DFT.addRow(v2);
            }
        } catch (Exception e) {
            System.out.println("Error");
        }
        
    }
